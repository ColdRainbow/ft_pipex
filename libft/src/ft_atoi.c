#include "libft.h"

int	ft_atoi(const char *str)
{
	int				a;
	long long int	b;

	a = 1;
	b = 0;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t'
			|| *str == '\v' || *str == '\f' || *str == '\r'))
		++str;
	if (*str == '-')
		a = -1;
	if (*str == '-' || *str == '+')
		++str;
	while (*str && *str >= '0' && *str <= '9')
	{
		if (b >= 922337203685477600)
			return (-1);
		if ((b * a) <= -922337203685477)
			return (0);
		b = b * 10 + (*str - 48);
		++str;
	}
	return (b * a);
}
